install.packages("igraph")
library(igraph)
## we use igraph’s graph.data.frame function, which takes two data frames: d and vertices.
## - 'd' describes the edges of the network. Its first two columns are the IDs of the source and the target node for each edge.
## - 'vertices' starts with a column of node IDs. Any following columns are interpreted as node attributes
net <- graph.data.frame(links, nodes, directed=T)
net

E(net)        # The edges of the "net" object
V(net)        # The vertices of the "net" object
E(net)$type   # Edge attribute "type"
V(net)$media  # Vertex attribute "media"

# You can also manipulate the network matrix directly:
net[1,]
net[5,7]

plot(net)

## Remove Loops
simplify(net, remove.loops = T, remove.multiple = F)

plot(net, edge.arrow.size = .4, vertex.label = NA)

## Plot with node names
plot(net, edge.arrow.size=.2, edge.color="orange",
     vertex.color="gray50", vertex.frame.color="#ffffff",
     vertex.label=V(net)$media, vertex.label.color="black")

## Color the nodes based on Media Type
colrs <- c("gray50", "tomato", "gold")
V(net)$color <- colrs[V(net)$media.type]

plot(net, edge.arrow.size=.2, edge.color="orange",
     vertex.color=V(net)$color, vertex.frame.color="#ffffff",
     vertex.label=V(net)$media, vertex.label.color="black")

## Node size varies with size of audience
V(net)$size = V(net)$audience.size*0.6

## Now you want to know strength of Relationship based on Weight
E(net)$width <- E(net)$weight/6

## Regenerate the plot
plot(net, edge.arrow.size=.2, edge.color="orange",
     vertex.color=V(net)$color, vertex.frame.color="#ffffff",
     vertex.label=V(net)$media, vertex.label.color="black")

