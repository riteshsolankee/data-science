## Notice that there are more links than unique from-to combinations. That means we have cases
## in the data where there are multiple links between the same two nodes. We will collapse all links
## of the same type between the same two nodes by summing their weights(links[,3]), using aggregate() by
## “from”, “to”, & “type”(links[,-3]):
links <- aggregate(links[,3], links[,-3], sum)

## Sort the dataframe
links <- links[order(links$from, links$to),]

## Name the aggregated column as 'weight'
colnames(links)[4] <- "weight"
## Removed the randomized autogenerated ID column due sorting
rownames(links) <- NULL

